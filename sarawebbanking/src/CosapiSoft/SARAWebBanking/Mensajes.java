public final static String getMessage(String codmsg) throws Exception {
	try {
		loadMessages();
		for (int i = 0; i < message.size(); i++) {
			String str = ((java.util.Vector) message.elementAt(i)).elementAt(0).toString();
			if (str.equals(codmsg)) {
				String msg = ((java.util.Vector) message.elementAt(i)).elementAt(1).toString();
				return msg;
			}
		}
		return "Código de Mensaje = " + codmsg + " - No tiene tiene mensaje asignado, ingrese a la opción Mensajes del Aplicativo de SARA Web Manager";
	}
	catch (Exception e) {
		e.printStackTrace();
		throw new Exception("... Mensajes -> getMessage(" + codmsg + ") - " + e.getMessage());
	}
}
